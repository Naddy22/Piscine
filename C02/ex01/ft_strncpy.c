#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int counter;

	counter = 0;
	while (counter < n && src[counter] != '\0')
	{
		dest[counter] = src[counter];
		counter++;
	}
	while (counter < n)
	{
		dest[counter] = '\0';
		counter++;
	}
	return (dest);
}

int	main(void)
{
	char src[8] = "BLOBLOB";
	char dest[6] = "Pafpa";

	ft_strncpy(dest, src, 4);
	printf("%s", dest);
	return (0);
}
